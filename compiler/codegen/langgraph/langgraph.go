package langgraph

import (
	"fmt"
	"sort"
	"strings"

	"github.com/thakee/orca/compiler/ast"
	"github.com/thakee/orca/compiler/codegen"
	"github.com/thakee/orca/compiler/codegen/python"
	"github.com/thakee/orca/compiler/diagnostic"
	"github.com/thakee/orca/compiler/token"
)

// LangGraphBackend generates Python/LangGraph code directly from the analyzed AST.
type LangGraphBackend struct {
	codegen.BaseBackend
}

// New creates a new LangGraph codegen backend for the given analyzed AST.
func New(program *ast.Program) codegen.CodegenBackend {
	return &LangGraphBackend{BaseBackend: codegen.BaseBackend{Program: program}}
}

// resolvedProviders holds the result of a single pass over model blocks,
// extracting known providers and emitting diagnostics for unknown ones.
type resolvedProviders struct {
	names []string                // sorted, deduplicated known provider names
	deps  []codegen.Dependency    // langchain-core + provider-specific deps
	diags []diagnostic.Diagnostic // errors for unknown providers
}

// Generate produces a tree-structured output targeting LangGraph.
func (b *LangGraphBackend) Generate() codegen.CodegenOutput {
	var diags []diagnostic.Diagnostic

	rp := b.resolveProviders()
	diags = append(diags, rp.diags...)

	return codegen.CodegenOutput{
		RootDir: codegen.OutputDirectory{
			Name: "build",
			Files: []codegen.OutputFile{
				{Name: "main.py", Content: b.generateMain(rp.names)},
			},
		},
		Dependencies: rp.deps,
		Diagnostics:  diags,
	}
}

// resolveProviders walks model blocks once to collect known providers,
// build the dependency list, and emit diagnostics for unknown providers.
func (b *LangGraphBackend) resolveProviders() resolvedProviders {
	seen := make(map[string]bool)
	var names []string
	var diags []diagnostic.Diagnostic

	for _, block := range b.CollectBlocks(token.MODEL) {
		expr, ok := block.GetFieldExpression("provider")
		if !ok {
			continue
		}
		sl, ok := expr.(*ast.StringLiteral)
		if !ok {
			continue
		}
		if _, known := providerRegistry[sl.Value]; !known {
			pos := sl.Start()
			diags = append(diags, diagnostic.Diagnostic{
				Severity: diagnostic.Error,
				Code:     diagnostic.CodeUnknownProvider,
				Position: diagnostic.Position{
					Line:   pos.Line,
					Column: pos.Column,
				},
				Message: fmt.Sprintf("unknown provider %q", sl.Value),
				Source:  "codegen",
			})
			continue
		}
		if !seen[sl.Value] {
			seen[sl.Value] = true
			names = append(names, sl.Value)
		}
	}
	sort.Strings(names)

	deps := []codegen.Dependency{{Name: "langchain-core"}}
	for _, dep := range providerDeps(names) {
		deps = append(deps, codegen.Dependency{Name: dep})
	}

	return resolvedProviders{names: names, deps: deps, diags: diags}
}

func (b *LangGraphBackend) generateMain(providers []string) string {
	var s strings.Builder

	s.WriteString("# Auto-generated by Orca compiler — do not edit\n")

	b.writeImports(&s, providers)

	// Write let variables.
	lets := b.CollectLets()
	if len(lets) > 0 {
		s.WriteString("\n# --- Variables ---\n")
		for _, block := range lets {
			for _, assign := range block.Assignments {
				s.WriteString("\n")
				fmt.Fprintf(&s, "%s = %s  # %s\n",
					assign.Name,
					python.OrcaToPythonExpression(assign.Value),
					codegen.SourceComment(block.SourceFile, assign.TokenStart.Line))
			}
		}
	}

	// Write model definitions.
	models := b.CollectBlocks(token.MODEL)
	if len(models) > 0 {
		s.WriteString("\n# --- Models ---\n")
		for _, block := range models {
			s.WriteString("\n")
			writeModel(&s, block)
		}
	}

	// Write agent definitions.
	agents := b.CollectBlocks(token.AGENT)
	if len(agents) > 0 {
		s.WriteString("\n# --- Agents ---\n")
		for _, block := range agents {
			s.WriteString("\n")
			writeAgent(&s, block)
		}
	}

	return s.String()
}

func (b *LangGraphBackend) writeImports(s *strings.Builder, providers []string) {
	for _, p := range providers {
		if info, ok := providerRegistry[p]; ok {
			s.WriteString("\n")
			s.WriteString(info.Import)
			s.WriteString("\n")
		}
	}
}

// writeModel generates a Python model instantiation from a model block.
func writeModel(s *strings.Builder, block *ast.BlockStatement) {
	var provider, modelName string
	var temperature float64
	var hasTemp bool

	for _, assign := range block.Assignments {
		switch assign.Name {
		case "provider":
			if sl, ok := assign.Value.(*ast.StringLiteral); ok {
				provider = sl.Value
			}
		case "model_name":
			if sl, ok := assign.Value.(*ast.StringLiteral); ok {
				modelName = sl.Value
			}
		case "temperature":
			switch e := assign.Value.(type) {
			case *ast.FloatLiteral:
				temperature = e.Value
				hasTemp = true
			case *ast.IntegerLiteral:
				temperature = float64(e.Value)
				hasTemp = true
			}
		}
	}

	info, ok := providerRegistry[provider]
	if !ok {
		return
	}

	var params []string
	params = append(params, fmt.Sprintf("model=%q", modelName))
	if hasTemp {
		params = append(params, fmt.Sprintf("temperature=%s", python.FormatFloat(temperature)))
	}

	fmt.Fprintf(s, "%s = %s(  # %s\n", block.Name, info.Class,
		codegen.SourceComment(block.SourceFile, block.TokenStart.Line))
	for _, p := range params {
		fmt.Fprintf(s, "    %s,\n", p)
	}
	s.WriteString(")\n")
}

// writeAgent generates a Python create_react_agent call from an agent block.
// Produces: name = create_react_agent(model, tools=[...], prompt="persona")
func writeAgent(s *strings.Builder, block *ast.BlockStatement) {
	var model, persona string
	var tools string

	for _, assign := range block.Assignments {
		switch assign.Name {
		case "model":
			model = python.OrcaToPythonExpression(assign.Value)
		case "persona":
			persona = python.OrcaToPythonExpression(assign.Value)
		case "tools":
			if list, ok := assign.Value.(*ast.ListLiteral); ok && len(list.Elements) > 0 {
				tools = python.OrcaToPythonExpression(assign.Value)
			}
		}
	}

	var params []string
	params = append(params, model)
	if tools != "" {
		params = append(params, fmt.Sprintf("tools=%s", tools))
	}
	params = append(params, fmt.Sprintf("prompt=%s", persona))

	fmt.Fprintf(s, "%s = create_react_agent(  # %s\n", block.Name,
		codegen.SourceComment(block.SourceFile, block.TokenStart.Line))
	for _, p := range params {
		fmt.Fprintf(s, "    %s,\n", p)
	}
	s.WriteString(")\n")
}

// providerDeps returns sorted, deduplicated pip dependencies.
func providerDeps(providers []string) []string {
	seen := make(map[string]bool)
	var deps []string
	for _, p := range providers {
		if info, ok := providerRegistry[p]; ok && !seen[info.Dep] {
			dep := info.Dep
			deps = append(deps, dep)
			seen[dep] = true
		}
	}
	sort.Strings(deps)
	return deps
}
