package langgraph

import (
	"fmt"
	"sort"
	"strings"

	"github.com/thakee/orca/compiler/codegen"
	"github.com/thakee/orca/compiler/ir"
)

// Backend generates Python/LangGraph code from the IR.
type Backend struct {
	program *ir.IR
}

// New creates a new LangGraph codegen backend for the given IR.
func New(program *ir.IR) *Backend {
	return &Backend{program: program}
}

// Generate produces Python code and project config targeting LangGraph.
func (b *Backend) Generate() codegen.Output {
	return codegen.Output{
		MainPy:        b.generateMain(),
		PyProjectTOML: b.generatePyProject(),
	}
}

func (b *Backend) generateMain() string {
	var s strings.Builder

	s.WriteString("# Auto-generated by Orca compiler — do not edit\n")
	b.writeImports(&s)

	if len(b.program.Lets) > 0 {
		s.WriteString("\n# --- Variables ---\n")
		for _, v := range b.program.Lets {
			s.WriteString("\n")
			writeLet(&s, v)
		}
	}

	if len(b.program.Models) > 0 {
		s.WriteString("\n# --- Models ---\n")
		for _, m := range b.program.Models {
			s.WriteString("\n")
			writeModel(&s, m)
		}
	}

	return s.String()
}

func (b *Backend) writeImports(s *strings.Builder) {
	// IR.Providers is pre-sorted.
	for _, p := range b.program.Providers {
		if imp, ok := providerImport[p]; ok {
			s.WriteString("\n")
			s.WriteString(imp)
			s.WriteString("\n")
		}
	}
}

func writeModel(s *strings.Builder, m ir.ModelDef) {
	// Unknown providers are caught by the analyzer — this is unreachable
	// in normal flow, but guard defensively.
	class := providerClass[m.Provider]
	if class == "" {
		return
	}

	var params []string
	params = append(params, fmt.Sprintf("model=%q", m.ModelName))
	if m.HasTemp {
		params = append(params, fmt.Sprintf("temperature=%s", codegen.FormatFloat(m.Temperature)))
	}

	fmt.Fprintf(s, "%s = %s(%s)  # %s\n", m.Name, class, strings.Join(params, ", "), codegen.SourceComment(m.File, m.Line))
}

func writeLet(s *strings.Builder, v ir.LetVar) {
	fmt.Fprintf(s, "%s = %s  # %s\n", v.Name, codegen.ExprToPython(v.Value), codegen.SourceComment(v.File, v.Line))
}

func (b *Backend) generatePyProject() string {
	var s strings.Builder

	s.WriteString("[project]\n")
	s.WriteString("name = \"orca-build\"\n")
	s.WriteString("version = \"0.1.0\"\n")
	s.WriteString("requires-python = \">=3.11\"\n")
	s.WriteString("dependencies = [\n")
	s.WriteString("    \"langchain-core\",\n")

	deps := providerDeps(b.program.Providers)
	for _, dep := range deps {
		fmt.Fprintf(&s, "    %q,\n", dep)
	}

	s.WriteString("]\n")

	return s.String()
}

// providerDeps returns sorted, deduplicated pip dependencies.
func providerDeps(providers []string) []string {
	seen := make(map[string]bool)
	var deps []string
	for _, p := range providers {
		if dep, ok := providerDep[p]; ok && !seen[dep] {
			deps = append(deps, dep)
			seen[dep] = true
		}
	}
	sort.Strings(deps)
	return deps
}
