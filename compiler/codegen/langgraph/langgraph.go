package langgraph

import (
	"fmt"
	"sort"
	"strings"

	"github.com/thakee/orca/compiler/ast"
	"github.com/thakee/orca/compiler/codegen"
	"github.com/thakee/orca/compiler/codegen/python"
	"github.com/thakee/orca/compiler/token"
)

// Backend generates Python/LangGraph code directly from the analyzed AST.
type Backend struct {
	program *ast.Program
}

// New creates a new LangGraph codegen backend for the given analyzed AST.
func New(program *ast.Program) *Backend {
	return &Backend{program: program}
}

// Generate produces Python code and project config targeting LangGraph.
func (b *Backend) Generate() python.Output {
	return python.Output{
		MainPy:        b.generateMain(),
		PyProjectTOML: b.generatePyProject(),
	}
}

func (b *Backend) generateMain() string {
	var s strings.Builder

	s.WriteString("# Auto-generated by Orca compiler — do not edit\n")

	providers := b.collectProviders()
	b.writeImports(&s, providers)

	// Write let variables.
	lets := b.collectLets()
	if len(lets) > 0 {
		s.WriteString("\n# --- Variables ---\n")
		for _, block := range lets {
			for _, assign := range block.Assignments {
				s.WriteString("\n")
				fmt.Fprintf(&s, "%s = %s  # %s\n",
					assign.Name,
					python.ExprToPython(assign.Value),
					codegen.SourceComment(block.SourceFile, assign.TokenStart.Line))
			}
		}
	}

	// Write model definitions.
	models := b.collectBlocks(token.MODEL)
	if len(models) > 0 {
		s.WriteString("\n# --- Models ---\n")
		for _, block := range models {
			s.WriteString("\n")
			writeModel(&s, block)
		}
	}

	return s.String()
}

// collectProviders returns sorted, unique provider names from model blocks.
func (b *Backend) collectProviders() []string {
	seen := make(map[string]bool)
	for _, block := range b.collectBlocks(token.MODEL) {
		for _, assign := range block.Assignments {
			if assign.Name == "provider" {
				if s, ok := assign.Value.(*ast.StringLiteral); ok {
					seen[s.Value] = true
				}
			}
		}
	}
	var providers []string
	for p := range seen {
		providers = append(providers, p)
	}
	sort.Strings(providers)
	return providers
}

// collectBlocks returns all block statements of the given token type.
func (b *Backend) collectBlocks(tokenType token.TokenType) []*ast.BlockStatement {
	var blocks []*ast.BlockStatement
	for _, stmt := range b.program.Statements {
		if block, ok := stmt.(*ast.BlockStatement); ok && block.TokenStart.Type == tokenType {
			blocks = append(blocks, block)
		}
	}
	return blocks
}

// collectLets returns all let block statements.
func (b *Backend) collectLets() []*ast.BlockStatement {
	return b.collectBlocks(token.LET)
}

func (b *Backend) writeImports(s *strings.Builder, providers []string) {
	for _, p := range providers {
		if imp, ok := providerImport[p]; ok {
			s.WriteString("\n")
			s.WriteString(imp)
			s.WriteString("\n")
		}
	}
}

// writeModel generates a Python model instantiation from a model block.
func writeModel(s *strings.Builder, block *ast.BlockStatement) {
	var provider, modelName string
	var temperature float64
	var hasTemp bool

	for _, assign := range block.Assignments {
		switch assign.Name {
		case "provider":
			if sl, ok := assign.Value.(*ast.StringLiteral); ok {
				provider = sl.Value
			}
		case "model_name":
			if sl, ok := assign.Value.(*ast.StringLiteral); ok {
				modelName = sl.Value
			}
		case "temperature":
			switch e := assign.Value.(type) {
			case *ast.FloatLiteral:
				temperature = e.Value
				hasTemp = true
			case *ast.IntegerLiteral:
				temperature = float64(e.Value)
				hasTemp = true
			}
		}
	}

	class := providerClass[provider]
	if class == "" {
		return
	}

	var params []string
	params = append(params, fmt.Sprintf("model=%q", modelName))
	if hasTemp {
		params = append(params, fmt.Sprintf("temperature=%s", python.FormatFloat(temperature)))
	}

	fmt.Fprintf(s, "%s = %s(%s)  # %s\n",
		block.Name, class, strings.Join(params, ", "),
		codegen.SourceComment(block.SourceFile, block.TokenStart.Line))
}

func (b *Backend) generatePyProject() string {
	var s strings.Builder

	s.WriteString("[project]\n")
	s.WriteString("name = \"orca-build\"\n")
	s.WriteString("version = \"0.1.0\"\n")
	s.WriteString("requires-python = \">=3.11\"\n")
	s.WriteString("dependencies = [\n")
	s.WriteString("    \"langchain-core\",\n")

	providers := b.collectProviders()
	deps := providerDeps(providers)
	for _, dep := range deps {
		fmt.Fprintf(&s, "    %q,\n", dep)
	}

	s.WriteString("]\n")

	return s.String()
}

// providerDeps returns sorted, deduplicated pip dependencies.
func providerDeps(providers []string) []string {
	seen := make(map[string]bool)
	var deps []string
	for _, p := range providers {
		if dep, ok := providerDep[p]; ok && !seen[dep] {
			deps = append(deps, dep)
			seen[dep] = true
		}
	}
	sort.Strings(deps)
	return deps
}
